package com.ta.pendaftaransiswa.model;

public class ProfileModel {
    private String npsn, nama_sekolah, alamat, kodepos, desa,
    kecamatan, kabupaten_kota, provinsi, status, waktu_penyelenggaraan,
    jenjang_pendidkan;


    public String getNpsn() {
        return npsn;
    }

    public void setNpsn(String npsn) {
        this.npsn = npsn;
    }

    public String getNama_sekolah() {
        return nama_sekolah;
    }

    public void setNama_sekolah(String nama_sekolah) {
        this.nama_sekolah = nama_sekolah;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getKodepos() {
        return kodepos;
    }

    public void setKodepos(String kodepos) {
        this.kodepos = kodepos;
    }

    public String getDesa() {
        return desa;
    }

    public void setDesa(String desa) {
        this.desa = desa;
    }

    public String getKecamatan() {
        return kecamatan;
    }

    public void setKecamatan(String kecamatan) {
        this.kecamatan = kecamatan;
    }

    public String getKabupaten_kota() {
        return kabupaten_kota;
    }

    public void setKabupaten_kota(String kabupaten_kota) {
        this.kabupaten_kota = kabupaten_kota;
    }

    public String getProvinsi() {
        return provinsi;
    }

    public void setProvinsi(String provinsi) {
        this.provinsi = provinsi;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getWaktu_penyelenggaraan() {
        return waktu_penyelenggaraan;
    }

    public void setWaktu_penyelenggaraan(String waktu_penyelenggaraan) {
        this.waktu_penyelenggaraan = waktu_penyelenggaraan;
    }

    public String getJenjang_pendidkan() {
        return jenjang_pendidkan;
    }

    public void setJenjang_pendidkan(String jenjang_pendidkan) {
        this.jenjang_pendidkan = jenjang_pendidkan;
    }
}
